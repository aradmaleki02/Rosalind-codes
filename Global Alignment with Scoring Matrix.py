scoring_matrix = {}
for i, line in enumerate(BLOSUM62.splitlines()):
    line = line.strip()
    if i == 0:
        keys = line.split()
    else:
        values = line.split()
        scoring_matrix[values[0]] = {keys[j]: int(values[j + 1]) for j in range(len(keys))}

string1 = ""
input()
while True:
    s = input()
    if s[0] == '>':
        break
    string1 += s
string2 = ""
while True:
    s = input()
    if not s:
        break
    string2 += s

n = len(string1)
m = len(string2)

distance_matrix = [[-5 * (i + j) for i in range(m + 1)] for j in range(n + 1)]

for i in range(1, n + 1):
    for j in range(1, m + 1):
        distance_matrix[i][j] = max(distance_matrix[i - 1][j] - 5, distance_matrix[i][j - 1] - 5,
                                    distance_matrix[i - 1][j - 1] + scoring_matrix[string1[i - 1]][string2[j - 1]])

print(distance_matrix[n][m])